/* POST handler                                                       */
/* ------------------------------------------------------------------ */

export async function POST(request: NextRequest) {
  try {
    let body: any;
    try {
      body = await request.json();
    } catch {
      return NextResponse.json({ error: "Invalid JSON body" }, { status: 400 });
    }

    const { name, email, phone, url, toolName, analysisData, scores, source } =
      body || {};

    // Validate required fields
    if (!name || typeof name !== "string" || !name.trim()) {
      return NextResponse.json(
        { error: "Missing required field: name" },
        { status: 400 }
      );
    }
    if (!email || typeof email !== "string" || !email.includes("@")) {
      return NextResponse.json(
        { error: "Missing or invalid required field: email" },
        { status: 400 }
      );
    }
    if (!url || typeof url !== "string" || !url.trim()) {
      return NextResponse.json(
        { error: "Missing required field: url" },
        { status: 400 }
      );
    }
    if (!toolName || typeof toolName !== "string" || !toolName.trim()) {
      return NextResponse.json(
        { error: "Missing required field: toolName" },
        { status: 400 }
      );
    }

    // 1. Save lead to database (non-fatal if it fails)
    try {
      const lead = await db.lead.create({
        data: {
          name: String(name).trim(),
          email: String(email).trim(),
          phone: phone ? String(phone).trim() : "",
          message: `${toolName} report request for ${url}`,
          source:
            source ||
            `analysis-tool-${toolName}`.toLowerCase().replace(/\s+/g, "-"),
          status: "new",
        },
      });

      // Fire-and-forget: notify admin about the new lead (non-fatal).
      // Don't await so the report-generation flow stays fast.
      notifyNewLead(lead).catch((e) =>
        console.error("[/api/report] notification error:", e)
      );
    } catch (dbError) {
      console.error("[/api/report] DB error (non-fatal):", dbError);
      // Continue — still generate + email the report
    }

    // 2. Build ReportData
    const coercedScores = coerceScores(scores || analysisData?.scores);
    const checks = coerceChecks(
      analysisData?.checks || analysisData?.performanceChecks || []
    ).concat(
      coerceChecks(analysisData?.seoChecks || []).map((c) => ({
        ...c,
        category: c.category === "General" ? "SEO" : c.category,
      }))
    );

    const summary = buildSummary(analysisData, url, toolName);
    const recommendations = buildRecommendations(
      analysisData?.recommendations || analysisData?.quickWins,
      checks
    );

    const reportData: ReportData = {
      toolName,
      url,
      analyzedAt: new Date(),
      scores: coercedScores,
      checks,
      summary,
      recommendations,
    };

    // 3. Generate PDF
    let pdfBuffer: Buffer;
    try {
      pdfBuffer = await generateAnalysisPDF(reportData);
    } catch (pdfErr) {
      console.error("[/api/report] PDF generation error:", pdfErr);
      return NextResponse.json(
        {
          error: "Failed to generate PDF report",
          details: pdfErr instanceof Error ? pdfErr.message : String(pdfErr),
        },
        { status: 500 }
      );
    }

    // 4. Send email with PDF attachment
    const reportFilename = `SOCIAL-VIENS-${toolName
      .replace(/[^a-zA-Z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .toUpperCase()}-Report.pdf`;

    const emailResult = await sendReportEmail({
      to: String(email).trim(),
      name: String(name).trim(),
      reportBuffer: pdfBuffer,
      reportFilename,
      toolName,
      url,
    });

    // 5. Build WhatsApp link
    const whatsappMessage = encodeURIComponent(
      `Hi! I'm ${name}. I just used the ${toolName} on your website. I'd like a detailed report for ${url}.`
    );
    const whatsappUrl = `https://wa.me/918178004800?text=${whatsappMessage}`;

    return NextResponse.json({
      success: true,
      message: "Report generated and emailed successfully",
      whatsappUrl,
      emailPreviewUrl: emailResult.previewUrl,
      emailSent: emailResult.success,
      emailProvider: emailResult.provider,
      emailError: emailResult.error,
      reportSizeBytes: pdfBuffer.length,
    });
  } catch (error: any) {
    console.error("[/api/report] Error:", error);
    return NextResponse.json(
      {
        error: "Failed to generate report",
        details: error?.message || String(error),
      },
      { status: 500 }
    );
  }
}
